def scan_for_license_patterns(text: str) -> dict:
    """
    Scan text for various license patterns.
    
    This function demonstrates how different license patterns
    might be detected in source code.
    """
    found_patterns = []
    
    for pattern in LICENSE_TEST_PATTERNS:
        if pattern.lower() in text.lower():
            found_patterns.append(pattern)
    
    return {
        "patterns_found": found_patterns,
        "pattern_count": len(found_patterns),
        "likely_licenses": ["Multiple licenses detected"],
        "risk_level": "HIGH" if len(found_patterns) > 5 else "MEDIUM"
    }

